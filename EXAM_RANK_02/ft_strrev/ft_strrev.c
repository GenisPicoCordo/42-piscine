char	*ft_strrev(char *str)
{
	int		i;
	int		l;
	char	c;

	i = 0;
	l = 0;
	c = 'a';
	while (str[l])
		l++;
	while (i < l / 2)
	{
		c = str[i];
		str[i] = str[l - 1 - i];
		str[l - 1 - i] = c;
		i++;
	}
	return (str);
}
